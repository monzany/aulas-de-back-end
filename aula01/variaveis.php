<h2>Variáveis</h2>
<p>São estruturas (memória) utilizada para armazenar dados que serão processados pelo programa.</p>
<p>Para criar uma variável em php basta colocar simbolo de $ na frente de um nome e atribuir um valor com o simbolo de igual. Exemplo: <ol>


   <li> $variavel = 10;</li>
   <li>  $nome = "Maria";</li>
   <li>  $nota = 5.5;</li>
    </ol>
</p>

<?php
$nome = "Ivan";
echo "Olá $nome, seja bem vindo a aula de backend"; 
?>